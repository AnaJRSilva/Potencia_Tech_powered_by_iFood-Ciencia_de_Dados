# Adição
print(1 + 1)
adição = 1 + 1
print(adição)

# Substração
print(1 - 1)
subtração = 1 - 1
print(subtração)

# Multiplicação
print(2 * 3)
multiplicação = 2 * 3
print(multiplicação)

# Divisão 
divisão_decimal = 6 / 3
print(divisão_decimal)
print(6 / 3)

divisão_inteira = 8 // 2
print(divisão_inteira)
print(8 // 2)

# Módulo: resto da divisão 
print(13 % 3)
módulo_resto = 13 % 3 
print(módulo_resto)

# Exponenciação 
print(2 ** 3)
exponenciação = 2  ** 3
print(exponenciação)

# Testando e relembrando sobre regras da matématica
print(10 - 5 * 2) # primeiro multiplicação, depos subtração
print((10 - 5) * 2) # primeiro o conteúdo dos parênteses
print(10 ** 2 * 2) # primeiro exponenciação depois multiplicação
print(10 ** (2 * 2)) # primeiro o conteúdo dos parênteses
print(10 / 2 * 4) # da esquerda para a direita

